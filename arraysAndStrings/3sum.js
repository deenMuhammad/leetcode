// Given an integer array nums, return all the triplets [nums[i], nums[j], nums[k]] such that i != j, i != k, and j != k, and nums[i] + nums[j] + nums[k] == 0.

// Notice that the solution set must not contain duplicate triplets.

// Example 1:

// Input: nums = [-1,0,1,2,-1,-4]
// Output: [[-1,-1,2],[-1,0,1]]
// Example 2:

// Input: nums = []
// Output: []
// Example 3:

// Input: nums = [0]
// Output: []

/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  let triplets = [];
  let taken = [];
  for (let index = 0; index < nums.length; index++) {
    let x = nums[index];
    let hashtable = {};
    for (let j = index; j < nums.length; j++) {
      //assign current element
      let elem = nums[j];
      let minusElem = 0 - x - elem;
      if (
        hashtable[minusElem] != undefined &&
        index != j &&
        index != nums.indexOf(minusElem)
      ) {
        let res = [x, minusElem, elem].sort(function (a, b) {
          return a - b;
        });
        if (!taken.includes(JSON.stringify(res))) {
          taken.push(JSON.stringify(res));
          triplets.push(res);
        }
      } else {
        hashtable[elem] = elem;
      }
    }
  }
  return triplets;
};

